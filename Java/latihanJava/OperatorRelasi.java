public class OperatorRelasi {
    public static void main (String[]args){
        int x=10;
        int y=30;
        System.out.println("x > y adalah " + (x > y));
        System.out.println("x > =y adalah " + (x >= y));
        System.out.println("x < y adalah " + (x < y));
        System.out.println("x <= y adalah " + (x <= y));
        System.out.println("x = y adalah " + (x == y));
        System.out.println("x != y adalah " + (x!= y));
        
    }
}
